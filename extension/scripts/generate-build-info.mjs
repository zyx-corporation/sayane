#!/usr/bin/env node
/** Emit extension/src/build-info.ts with version and latest source update time. */

import { execSync } from "node:child_process";
import { readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const extensionDir = join(__dirname, "..");
const repoRoot = join(extensionDir, "..");
const srcDir = join(extensionDir, "src");

function gitLatestIso(pathInRepo) {
  try {
    const out = execSync(
      `git -C ${JSON.stringify(repoRoot)} log -1 --format=%cI -- ${JSON.stringify(pathInRepo)}`,
      { encoding: "utf8", stdio: ["ignore", "pipe", "ignore"] },
    ).trim();
    return out || null;
  } catch {
    return null;
  }
}

function maxMtimeIso(dir) {
  let latest = 0;
  const walk = (p) => {
    for (const ent of readdirSync(p, { withFileTypes: true })) {
      const full = join(p, ent.name);
      if (ent.isDirectory()) walk(full);
      else {
        const m = statSync(full).mtimeMs;
        if (m > latest) latest = m;
      }
    }
  };
  walk(dir);
  return new Date(latest || Date.now()).toISOString();
}

function parseIsoMs(iso) {
  if (!iso) return 0;
  const ms = Date.parse(iso.replace(/([+-]\d{2}):(\d{2})$/, "$1$2"));
  return Number.isNaN(ms) ? 0 : ms;
}

function latestSourceUpdateIso(...candidates) {
  let best = "";
  let bestMs = 0;
  for (const iso of candidates) {
    if (!iso) continue;
    const ms = parseIsoMs(iso);
    if (ms >= bestMs) {
      bestMs = ms;
      best = iso;
    }
  }
  return best || maxMtimeIso(srcDir);
}

const pkg = JSON.parse(readFileSync(join(extensionDir, "package.json"), "utf8"));
const sourceUpdatedAt = latestSourceUpdateIso(
  gitLatestIso("extension/src"),
  gitLatestIso("extension"),
  maxMtimeIso(srcDir),
);

const content = `/** Auto-generated by scripts/generate-build-info.mjs — do not edit manually. */

export const EXTENSION_BUILD_INFO = {
  version: ${JSON.stringify(pkg.version)},
  sourceUpdatedAt: ${JSON.stringify(sourceUpdatedAt)},
  component: "sayane-extension",
} as const;
`;

writeFileSync(join(srcDir, "build-info.ts"), content, "utf8");
